#ejercicio con operadores aritmeticos (modulos,exponente, division entera)
#entrada
numero1 = int(input("ingrese el primer número: "))
numero2 = int(input("ingrese el segundo número: "))
#proceso
divisonentera = numero1 // numero2
exponente = numero1 ** numero2
modulo = numero1 % numero2
#salida
print("El resultado de la division entera es: " + str(divisonentera))
print("El resultado del exponente es: " + str(exponente))
print("El resultado del modulo es: " + str(modulo))

